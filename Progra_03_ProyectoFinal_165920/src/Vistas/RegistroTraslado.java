    @Override
    public void actionPerformed(ActionEvent ae) {
        String action = ae.getActionCommand();

        switch (action) {

            case "Registrar":
                registrar();
                break;
        }
    }

    private void fillComboBox() {
        DefaultComboBoxModel model = new DefaultComboBoxModel();

        for (Residuo r : residuoControl.findAll()) {
            model.addElement(r.getNombre() + "  (" + r.getCodigo() + ")");
        }

        jComboBoxResiduos.setModel(model);
    }

    private void registrar() {
        String origen = jTextFieldOrigen.getText();
        String destino = jTextFieldDestino.getText();
        String tratamiento = jTextFieldTratamiento.getText();
        String medioTransporte = jTextFieldMedioTransporte.getText();

        Date salida = null;
        Date llegada = null;
        boolean salidaMatch = Regex.Regex.matchDate(jTextFieldFechaYear.getText(), jTextFieldFechaMonth.getText(), jTextFieldFechaDay.getText());
        boolean llegadaMatch = Regex.Regex.matchDate(jTextFieldFechaLlegadaYear.getText(), jTextFieldFechaLlegadaMonth.getText(), jTextFieldFechaLlegadaDay.getText());

        if (salidaMatch && llegadaMatch) {
            int salidaDay = Integer.parseInt(jTextFieldFechaDay.getText());
            int salidaMonth = Integer.parseInt(jTextFieldFechaMonth.getText());
            int salidaYear = Integer.parseInt(jTextFieldFechaYear.getText());
            salida = new Date(salidaYear - 1900, salidaMonth - 1, salidaDay);

            int llegadaDay = Integer.parseInt(jTextFieldFechaLlegadaDay.getText());
            int llegadaMonth = Integer.parseInt(jTextFieldFechaLlegadaMonth.getText());
            int llegadaYear = Integer.parseInt(jTextFieldFechaLlegadaYear.getText());
            llegada = new Date(llegadaYear - 1900, llegadaMonth - 1, llegadaDay);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Ingrese fecha valida por favor!");
        }

        Residuo residuo = getResiduoSeleccionado();
        Traslado t = new Traslado(residuo, origen, destino, salida, llegada, tratamiento, transportadora, medioTransporte);
        boolean b = trasladorControl.insertOne(t);
        if (b) {
            this.setVisible(false);
            JOptionPane.showMessageDialog(rootPane, "Traslado Registrado Exitosamente!");

        } else {
            JOptionPane.showMessageDialog(rootPane, "Traslado no se Registr√≥ Exitosamente!");
        }
    }

    private Residuo getResiduoSeleccionado() {
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        String s = (String) jComboBoxResiduos.getSelectedItem();
        s = s.split("[\\(\\)]")[1];
        int codigo = Integer.parseInt(s);

        return residuoControl.findByCode(codigo);
    }

